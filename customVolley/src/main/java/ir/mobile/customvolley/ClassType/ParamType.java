package ir.mobile.customvolley.ClassType;

public class ParamType {
    private String kry;
    private String value;
    public  ParamType(String kry,String value){
        this.kry=kry;
        this.value=value;
    }

    public String getValue() {
        return value;
    }

    public String getKry() {
        return kry;
    }
}
